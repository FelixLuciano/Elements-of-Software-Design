# Apaga ocorrências repetidas
# Faça uma função que recebe uma string contendo um texto e retorna uma nova string mantendo apenas a primeira ocorrência de cada caractere inalterada e substituindo as próximas ocorrências pelo caractere '*'. Exemplos:
# - Entrada: 'banana'
#   - Saída: 'ban***'
# - Entrada: 'abacaxi'
